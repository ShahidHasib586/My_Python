"""
An user will give a input number, we need to check whether the input number is positive negative or zero
"""

print("Please Enter the Alphabet")

character = input()

if character >='A' and character <='Z' :
    print("You have entered an Upper Case character")


elif character >='a' and character <='z':
    print("You have entered an Lower Case character")

else: 
    
    print("invalid input")
    
    
