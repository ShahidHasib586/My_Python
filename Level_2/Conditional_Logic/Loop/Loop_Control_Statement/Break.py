'''
To control a loop we use loop control statement
'''
# Break

'''When loop finds break statement inside a loop it stops executing the loop and goes for next statement'''

for num in range (1, 11):
    
    if num == 5:
        break
    print(num)
    
''' when number is equal to 5 loop is breaked'''
